// Compact weapon constructor matching corebook weapon code order:
// Name • Type • WA • Concealability • Availability • Damage • Ammo • #Shots • ROF • Reliability • Range • Cost
function w(
  templateId: string,
  name: string,
  type: WeaponType,
  wa: number,
  con: Concealability,
  avail: Availability,
  damage: string,
  ammo: string,
  shots: number,
  rof: number,
  rel: Reliability,
  range: number,
  cost: number,
  description: string,
  opts?: { skill?: string },
): WeaponTemplate {
  return {
    templateId,
    name,
    type,
    skill: opts?.skill ?? skillForType(type),
    wa,
    concealability: con,
    availability: avail,
    damage,
    ammo,
    shots,
    rof,
    reliability: rel,
    range,
    cost,
    description,
    melee: type === "melee",
    smartchipped: false,
  };
}

