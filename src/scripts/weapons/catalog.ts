// --- Types ---

export type WeaponType = "P" | "SMG" | "RIF" | "SHT" | "HVY" | "EX" | "melee";

export type Concealability = "P" | "J" | "L" | "N";

export type Reliability = "VR" | "ST" | "UR";

export interface WeaponTemplate {
  templateId: string;
  name: string;
  type: WeaponType;
  skill: string;
  wa: number;
  concealability: Concealability;
  availability: Availability;
  damage: string;
  ammo: string;
  shots: number;
  rof: number;
  reliability: Reliability;
  range: number;
  cost: number;
  description: string;
  melee: boolean;
  smartchipped: boolean;
}

