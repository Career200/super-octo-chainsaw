export const RELIABILITY_LABELS: Record<Reliability, string> = {
  VR: "Very Reliable",
  ST: "Standard",
  UR: "Unreliable",
};

// --- Helpers ---

export function skillForType(type: WeaponType): string {
  switch (type) {
    case "P":
      return "Handgun";
    case "SMG":
      return "Submachinegun";
    case "RIF":
      return "Rifle";
    case "SHT":
      return "Rifle";
    case "HVY":
      return "Heavy Weapons";
    case "EX":
      return "Archery"; // close enough for now, we'll pass custom skill for exotics that use other skills
    case "melee":
      return "Melee";
  }
}

